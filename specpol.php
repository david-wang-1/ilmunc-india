                    <h4> Topic B: Maritime Dispute</h4>
                    <p>In recent times, there has been a rise in conflicts related to maritime boundaries in territorial waters. Most of these controversies are due to conflicts between territorial sovereignty, based on history and legacy, and jurisdictional rights and interests in maritime boundaries. Maritime disputes are not an issue to be taken lightly when there are millions of oil barrels and thousands of tons of fish at stake for nations. For many countries, the oceans and seas play a large role in maintaining a functional economy. The United Nations Convention on the Law of the Sea, signed in 1982, defines the rights and responsibilities of nations with respect to their use of the world's oceans, establishing guidelines for businesses, the environment, and the management of marine natural resources. However, this agreement has done little to settle many of the debates still occurring today. A 2009 estimate states that 259 of the world’s 427 boundary disputes remain unsolved today. Currently, six countries lay overlapping claims to the East and South China Seas, an area that is rich in hydrocarbons and natural gas and through which trillions of dollars of global trade flow. Peru and Chile continue to disagree on rights over approximately 37,900 square kilometers of the Pacific Ocean. SPECPOL’s history of tackling global disputes of this nature lend it to be the perfect committee to tackle this debate. 
