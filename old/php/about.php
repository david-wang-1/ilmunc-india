<p>The Ivy League Model United Nations Conference India will uphold the University of Pennsylvania International Affairs Association’s tradition of hosting the largest and most prestigious Model United Nations simulations in the world. Conferences such as ILMUNC Philadelphia and UPMUNC together have celebrated over 75 years of success in hosting Model United Nations Conferences. With over 5000 delegates per year and representation from numerous countries around the world, ILMUNC and UPMUNC have raised the bar for MUN substantively, technologically, and organizationally to be recognized as the Championship Conferences in their respective segments. We are bringing this same championship experience to India through ILMUNC India 2015.</p>

